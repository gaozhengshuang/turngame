#!/bin/bash

##############################################################
#
# 统计本地修改的代码
#
##############################################################

codedirs='
gateserver
loginserver
matchserver
roomserver
def
robot
'

function svn_st()
{
    for dir in ${codedirs}; do
        svn st ${dir}/*.go | sed '/^?/d'
    done
}

echo '+-------------------------------------------------------------+'
echo '|                        svn ci 列表                          |'
echo '+-------------------------------------------------------------+'

echo `svn_st | sed 's/^[MACD]//g'`

echo '+-------------------------------------------------------------+'
echo '|                         详细列表                            |'
echo '+-------------------------------------------------------------+'

svn_st

echo '+-------------------------------------------------------------+'

